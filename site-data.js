function createMedia(item) {
  if (item.kind === "video") {
    return `<iframe class="bili-embed" src="${item.media}" title="${item.title}" allowfullscreen="allowfullscreen" scrolling="no" frameborder="0"></iframe>`;
  }
  return `<img src="${item.media}" alt="${item.title}">`;
}

function createWorkTitle(title) {
  const [name, label] = title.split("｜");
  if (!label) return `<h3 class="work-entry-title"><span>${title}</span></h3>`;
  return `<h3 class="work-entry-title"><span>${name}</span><b>|</b><small>${label}</small></h3>`;
}

function renderWorkCards(targetSelector, works = siteData.featuredWorks) {
  const target = document.querySelector(targetSelector);
  if (!target) return;

  const group = target.dataset.group;
  const items = group ? works.filter((item) => item.group === group) : works;

  target.innerHTML = items
    .map(
      (item) =>
        item.kind === "video"
          ? `
            <article class="work-entry video-work-card">
              ${item.hideTitleInCard ? "" : createWorkTitle(item.title)}
              <div class="video-copy work-text-card">
                <p>${item.desc}</p>
              </div>
              <a class="work-media" href="works.html" aria-label="查看 ${item.title}">
                ${createMedia(item)}
              </a>
            </article>
          `
          : `
            <article class="work-entry">
              ${createWorkTitle(item.title)}
              <div class="work-copy work-text-card">
                <p class="eyebrow">${item.type}</p>
                <p>${item.desc}</p>
                <div class="tags">${item.tags.map((tag) => `<b>${tag}</b>`).join("")}</div>
              </div>
              <a class="work-media" href="works.html" aria-label="查看 ${item.title}">
                ${createMedia(item)}
              </a>
            </article>
          `
    )
    .join("");
}

document.addEventListener("DOMContentLoaded", () => {
  document.querySelectorAll("[data-work-grid]").forEach((grid) => {
    renderWorkCards(`[data-work-grid="${grid.dataset.workGrid}"]`);
  });

  document.querySelectorAll("video").forEach((video) => {
    if (video.hasAttribute("controls")) return;
    video.addEventListener("mouseenter", () => video.play());
    video.addEventListener("mouseleave", () => {
      video.pause();
      video.currentTime = 0;
    });
  });
});
